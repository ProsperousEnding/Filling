const CONTENT_ROOT_PREFIX = '/blog/content/'

function sortArticlesByDate(articles) {
  return articles.sort((a, b) => new Date(b.date || 0) - new Date(a.date || 0))
}

function sortContentEntriesByDate(entries) {
  return entries.sort((left, right) => (
    new Date(right.createdAt || 0) - new Date(left.createdAt || 0)
    || String(left.title || '').localeCompare(String(right.title || ''), 'zh-CN')
  ))
}

function normalizeSearchField(input) {
  return String(input || '')
    .replace(/\r\n/g, '\n')
    .replace(/\s+/g, ' ')
    .trim()
    .toLowerCase()
}

function normalizeContentField(input) {
  return String(input || '')
    .replace(/\r\n/g, '\n')
    .replace(/\s+/g, ' ')
    .trim()
}

function normalizeDateField(input) {
  if (input instanceof Date && Number.isFinite(input.getTime())) {
    return input.toISOString()
  }

  return normalizeContentField(input)
}

function createExcerpt(text, maxLength = 160) {
  const normalizedText = normalizeContentField(text)

  if (!normalizedText) {
    return ''
  }

  if (normalizedText.length <= maxLength) {
    return normalizedText
  }

  return `${normalizedText.slice(0, maxLength).trim()}...`
}

function serializeGeneratedModule(variableName, payload) {
  return `// This file is auto-generated by scripts/build-content-index.mjs
// Do not edit it manually.
const ${variableName} = ${JSON.stringify(payload, null, 2)}

export default ${variableName}
`
}

function createContentIndexArticle(article) {
  const {
    plainText,
    ...contentIndexArticle
  } = article

  return contentIndexArticle
}

function normalizeNamedEntity(value) {
  if (!value) {
    return null
  }

  if (typeof value === 'string') {
    const name = normalizeContentField(value)

    if (!name) {
      return null
    }

    return {
      id: toSlugId(name),
      name
    }
  }

  const name = normalizeContentField(value.name || value.label || value.title)

  if (!name) {
    return null
  }

  return {
    id: normalizeContentField(value.id || toSlugId(name)),
    name
  }
}

function normalizeNamedEntityList(values = []) {
  return (Array.isArray(values) ? values : [])
    .map(normalizeNamedEntity)
    .filter(Boolean)
}

function createContentIndexRecord({
  id,
  kind,
  iconKind,
  title,
  description,
  excerpt,
  createdAt,
  updatedAt,
  category,
  tags,
  license,
  cover,
  coverSource,
  to,
  sectionTitle,
  sectionPath,
  sourcePath
}) {
  const normalizedTitle = normalizeContentField(title)
  const normalizedDescription = normalizeContentField(description)
  const normalizedExcerpt = normalizeContentField(excerpt || normalizedDescription)
  const normalizedCategory = normalizeNamedEntity(category)
  const normalizedTags = normalizeNamedEntityList(tags)

  return {
    id: normalizeContentField(id),
    kind: normalizeContentField(kind),
    iconKind: normalizeContentField(iconKind),
    title: normalizedTitle,
    description: normalizedDescription,
    excerpt: normalizedExcerpt,
    createdAt: normalizeDateField(createdAt) || null,
    updatedAt: normalizeDateField(updatedAt) || null,
    category: normalizedCategory,
    tags: normalizedTags,
    license: license && typeof license === 'object'
      ? {
        name: normalizeContentField(license.name),
        url: normalizeContentField(license.url)
      }
      : null,
    cover: normalizeContentField(cover),
    coverSource: normalizeContentField(coverSource),
    to: normalizeContentField(to),
    sectionTitle: normalizeContentField(sectionTitle),
    sectionPath: normalizeContentField(sectionPath),
    sourcePath: normalizeContentField(sourcePath)
  }
}

function createSearchIndexRecord(contentRecord, plainText, kindOverride = '') {
  const normalizedTitle = normalizeContentField(contentRecord?.title)
  const normalizedDescription = normalizeContentField(contentRecord?.description)
  const normalizedExcerpt = normalizeContentField(
    contentRecord?.excerpt || normalizedDescription || createExcerpt(plainText)
  )
  const normalizedCategory = normalizeNamedEntity(contentRecord?.category)
  const normalizedTags = normalizeNamedEntityList(contentRecord?.tags)
  const normalizedLicense = normalizeNamedEntity(contentRecord?.license)
  const normalizedSectionTitle = normalizeContentField(contentRecord?.sectionTitle)
  const normalizedPlainText = normalizeContentField(plainText)

  return {
    ...contentRecord,
    kind: normalizeContentField(kindOverride || contentRecord.kind),
    excerpt: normalizedExcerpt,
    plainText: normalizedPlainText,
    titleHaystack: normalizeSearchField(normalizedTitle),
    metaHaystack: normalizeSearchField([
      normalizedDescription,
      normalizedExcerpt,
      normalizedCategory?.name,
      normalizedLicense?.name,
      ...normalizedTags.map(tag => tag.name),
      normalizedSectionTitle
    ]
      .filter(Boolean)
      .join(' ')),
    contentHaystack: normalizeSearchField(normalizedPlainText)
  }
}

function createArticleContentRecord(article, routePatterns) {
  return createContentIndexRecord({
    id: `article:${article.id}`,
    kind: 'article',
    iconKind: 'article',
    title: article.title,
    description: article.description,
    excerpt: article.excerpt,
    createdAt: article.createdAt || article.date,
    updatedAt: article.updatedAt,
    category: article.category,
    tags: article.tags,
    license: article.license,
    cover: article.cover,
    coverSource: article.coverSource,
    to: getArticlePath(article, routePatterns),
    sectionTitle: '',
    sectionPath: '',
    sourcePath: article.sourcePath
  })
}

function resolveContentRelativePath(sourcePath = '') {
  if (!sourcePath.startsWith(CONTENT_ROOT_PREFIX)) {
    return ''
  }

  return sourcePath.slice(CONTENT_ROOT_PREFIX.length)
}

function resolveFallbackSectionTitle(sourcePath = '') {
  const relativePath = resolveContentRelativePath(sourcePath)
  const segments = relativePath.split('/').filter(Boolean)

  if (segments.length <= 1) {
    return ''
  }

  return segments[0]
}

function resolveSearchTarget(sourcePath, page, detailTo = '') {
  if (detailTo) {
    return detailTo
  }

  return normalizeContentField(page?.path)
}

function createMenuContextContentRecord(pageRecord, sourcePath, page = null) {
  const title = normalizeContentField(pageRecord.title)
  const description = normalizeContentField(pageRecord.description)

  return createContentIndexRecord({
    id: `page:${page?.key || normalizeMenuCollectionItemId(sourcePath)}`,
    kind: normalizeContentField(pageRecord.kind) || 'page',
    iconKind: normalizeContentField(pageRecord.iconKind || pageRecord.kind || 'entry'),
    title,
    description,
    excerpt: description || createExcerpt(pageRecord.plainText),
    createdAt: pageRecord.createdAt,
    category: pageRecord.category,
    tags: pageRecord.tags,
    cover: pageRecord.cover,
    coverSource: pageRecord.coverSource,
    to: resolveSearchTarget(sourcePath, page),
    sectionTitle: page?.title && page.title !== pageRecord.title ? page.title : '',
    sectionPath: page?.path || '',
    sourcePath
  })
}

function createMenuCollectionContentRecord(itemRecord, sourcePath, page = null) {
  const category = normalizeNamedEntity(itemRecord?.category?.label)
  const tags = normalizeNamedEntityList(
    Array.isArray(itemRecord?.tags)
      ? itemRecord.tags.map(tag => tag?.label)
      : []
  )
  const fallbackSectionTitle = resolveFallbackSectionTitle(sourcePath)
  const title = normalizeContentField(itemRecord.title)
  const description = normalizeContentField(itemRecord.description)

  return createContentIndexRecord({
    id: `page-item:${page?.key || fallbackSectionTitle || 'content'}:${itemRecord.itemId}`,
    kind: normalizeContentField(itemRecord.kind) || 'entry',
    iconKind: normalizeContentField(itemRecord.iconKind || itemRecord.kind || 'entry'),
    title,
    description,
    excerpt: normalizeContentField(itemRecord.detailDescription) || description || createExcerpt(itemRecord.plainText),
    createdAt: itemRecord.date,
    category,
    tags,
    cover: itemRecord.cover,
    coverSource: itemRecord.coverSource,
    to: resolveSearchTarget(sourcePath, page, itemRecord.to),
    sectionTitle: page?.title || fallbackSectionTitle,
    sectionPath: page?.path || '',
    sourcePath
  })
}

function toPosixRelativePath(absolutePath) {
  return path.relative(ROOT_DIR, absolutePath).split(path.sep).join('/')
}

function toSourcePath(absolutePath) {
  return `/${toPosixRelativePath(absolutePath)}`
}

async function collectMarkdownFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true })
  const nestedEntries = await Promise.all(entries.map(async (entry) => {
    const absolutePath = path.join(directory, entry.name)

    if (entry.isDirectory()) {
      return collectMarkdownFiles(absolutePath)
    }

    if (entry.isFile() && entry.name.endsWith('.md')) {
      return [absolutePath]
    }

    return []
  }))

  return nestedEntries.flat().sort((left, right) => left.localeCompare(right, 'en'))
}

async function loadSiteConfig() {
  try {
    const rawConfig = await readFile(SITE_CONFIG_FILE, 'utf8')
    return parseToml(rawConfig)
  } catch {
    return {}
  }
}

async function loadLicenseConfig() {
  try {
    const rawConfig = await readFile(LICENSE_CONFIG_FILE, 'utf8')
    return parseToml(rawConfig)
  } catch {
    return {}
  }
}

async function loadCoverConfig() {
  try {
    const rawConfig = await readFile(COVER_CONFIG_FILE, 'utf8')
    return normalizeCoverConfig(parseToml(rawConfig))
  } catch {
    return normalizeCoverConfig({})
  }
}

async function loadMarkdownConfig() {
  try {
    const rawConfig = await readFile(MARKDOWN_CONFIG_FILE, 'utf8')
    return normalizeMarkdownConfig(parseToml(rawConfig))
  } catch {
    return normalizeMarkdownConfig({})
  }
}

function createSearchContext(siteConfig = {}) {
  const routePatterns = normalizeBlogRoutePatterns(siteConfig?.routing || {})
  const menuPages = resolveMenuPages(siteConfig?.menus || {}, routePatterns)
    .filter(page => !page.builtIn)
  const filePageLookup = new Map()
  const folderPageEntries = []

  menuPages.forEach((page) => {
    const resolvedFilePath = resolveMenuContentSourcePath(page.file)
    const resolvedFolderPrefix = resolveMenuContentFolderPrefix(page.folder)

    if (resolvedFilePath) {
      filePageLookup.set(resolvedFilePath, page)
    }

    if (resolvedFolderPrefix) {
      folderPageEntries.push({
        page,
        folderPrefix: resolvedFolderPrefix
      })
    }
  })

  folderPageEntries.sort((left, right) => right.folderPrefix.length - left.folderPrefix.length)

  return {
    routePatterns,
    filePageLookup,
    folderPageEntries
  }
}

function resolveMappedMenuPage(sourcePath, searchContext) {
  const filePage = searchContext.filePageLookup.get(sourcePath)

  if (filePage) {
    return {
      page: filePage,
      sourceType: 'page'
    }
  }

  const folderEntry = searchContext.folderPageEntries.find(({ folderPrefix }) => (
    isMenuSourcePathInFolder(sourcePath, folderPrefix)
  ))

  if (folderEntry) {
    return {
      page: folderEntry.page,
      sourceType: 'page-item'
    }
  }

  return null
}

function isArticleSourcePath(sourcePath = '') {
  return sourcePath.startsWith(`${CONTENT_ROOT_PREFIX}articles/`)
}

async function writeGeneratedModuleIfChanged(filePath, content) {
  let previousContent = ''
  try {
    previousContent = await readFile(filePath, 'utf8')
  } catch {
    previousContent = ''
  }

  if (previousContent !== content) {
    await writeFile(filePath, content, 'utf8')
  }
}

export async function generateContentIndex() {
  const [siteConfig, licenseConfig, coverConfig, markdownConfig, markdownFiles] = await Promise.all([
    loadSiteConfig(),
    loadLicenseConfig(),
    loadCoverConfig(),
    loadMarkdownConfig(),
    collectMarkdownFiles(CONTENT_DIR)
  ])
  const searchContext = createSearchContext(siteConfig)
  const articles = []
  const contentEntries = []
  const searchEntries = []

  for (const absolutePath of markdownFiles) {
    const sourcePath = toSourcePath(absolutePath)
    const rawContent = await readFile(absolutePath, 'utf8')

    if (isArticleSourcePath(sourcePath)) {
      const article = parseArticleMetadata(rawContent, sourcePath, {
        defaultLicense: licenseConfig,
        markdownConfig,
        coverConfig
      })
      const contentEntry = createArticleContentRecord(article, searchContext.routePatterns)
      articles.push(article)
      contentEntries.push(contentEntry)
      searchEntries.push(createSearchIndexRecord(contentEntry, article.plainText, 'article'))
      continue
    }

    const mappedPage = resolveMappedMenuPage(sourcePath, searchContext)

    if (mappedPage?.sourceType === 'page-item') {
      const item = parseMenuCollectionDetail(rawContent, sourcePath, {
        pagePath: mappedPage.page.path,
        markdownConfig,
        coverConfig
      })
      const contentEntry = createMenuCollectionContentRecord(item, sourcePath, mappedPage.page)

      if (contentEntry.to) {
        contentEntries.push(contentEntry)
      }

      searchEntries.push(createSearchIndexRecord(contentEntry, item.plainText, 'page-item'))
      continue
    }

    if (mappedPage?.sourceType === 'page') {
      const pageRecord = parseMenuContextSource(rawContent, sourcePath, { markdownConfig, coverConfig })
      const contentEntry = createMenuContextContentRecord(pageRecord, sourcePath, mappedPage.page)

      if (contentEntry.to) {
        contentEntries.push(contentEntry)
      }

      searchEntries.push(createSearchIndexRecord(contentEntry, pageRecord.plainText, 'page'))
      continue
    }

    const relativePath = resolveContentRelativePath(sourcePath)
    const segments = relativePath.split('/').filter(Boolean)

    if (segments.length <= 1) {
      const pageRecord = parseMenuContextSource(rawContent, sourcePath, { markdownConfig, coverConfig })
      const contentEntry = createMenuContextContentRecord(pageRecord, sourcePath)

      if (contentEntry.to) {
        contentEntries.push(contentEntry)
      }

      searchEntries.push(createSearchIndexRecord(contentEntry, pageRecord.plainText, 'page'))
      continue
    }

    const item = parseMenuCollectionDetail(rawContent, sourcePath, { pagePath: '', markdownConfig, coverConfig })
    const contentEntry = createMenuCollectionContentRecord(item, sourcePath)

    if (contentEntry.to) {
      contentEntries.push(contentEntry)
    }

    searchEntries.push(createSearchIndexRecord(contentEntry, item.plainText, 'page-item'))
  }

  const sortedArticles = sortArticlesByDate(articles)
  const sortedContentEntries = sortContentEntriesByDate(contentEntries)
  const contentIndexPayload = {
    articles: sortedArticles.map(createContentIndexArticle),
    entries: sortedContentEntries
  }
  const searchIndexPayload = {
    entries: searchEntries
  }
  const serializedContentIndex = serializeGeneratedModule('contentIndex', contentIndexPayload)
  const serializedSearchIndex = serializeGeneratedModule('searchIndex', searchIndexPayload)

  await mkdir(path.dirname(CONTENT_INDEX_OUTPUT_FILE), { recursive: true })

  await writeGeneratedModuleIfChanged(CONTENT_INDEX_OUTPUT_FILE, serializedContentIndex)
  await writeGeneratedModuleIfChanged(SEARCH_INDEX_OUTPUT_FILE, serializedSearchIndex)

  return {
    contentIndex: contentIndexPayload,
    searchIndex: searchIndexPayload
  }
}

